# Django Admin is completely disabled in favor of custom admin interface
# All Django admin functionality has been replaced with custom admin views

# The custom admin interface can be accessed at:
# /admin-login/ - Custom admin login
# /admin-dashboard/ - Custom admin dashboard  
# /admin-products/ - Product management
# /admin-orders/ - Order management
# /admin-customers/ - Customer management
# /admin-contacts/ - Contact/message management

# Django admin is still available at /django-admin/ if needed for emergency access
